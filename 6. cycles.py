# следующие примеры диапазонов - идентичны
# range(0,10,1) - указаны начальное значение, конечное и шаг
# range(0,10) - начальньное и конечное значение. шаг по-умолчанию = 1
# range(10) - только конечное значение. начальное по-умолчанию = 0, шаг = 1

print("Несколько примеров вывода четных чисел")
for i in range(10):
    if i % 2 == 0:
        print(i, end=" ")
print("")

for i in range(10):
    # если число НЕчетное - пропустить итерацию
    if i % 2 != 0:
        continue
    # иначе - выполняем дальше - выводим число
    print(i, end=" ")
print("")

for i in range(0, 10, 2):
    print(i, end=" ")
print("")

print("Выводим числа в обратном порядке")
for i in range(10, 0, -1):
    print(i, end=" ")

# range(10,-11) - плохо!!! нужно указывать шаг! причем отрицательный!
for i in range(10, -11):
    print(i, end=" ")

# range(10, -11, 2) - плохо!!! нужен отрицательный шаг
for i in range(10, -11, 2):
    print(i, end=" ")

print("\n\n@@@@@@@@@@@@\n")

