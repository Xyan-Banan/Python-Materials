print("Пример вложенных циклов с условиями и операторами break/continue")
for i in range(3):
    print(i, end=" ")
    for j in range(10):
        # если сумма i + j нечетная - пропустить итерацию
        if (i + j) % 2 == 1:
            continue
        # иначе вывести число и продолжить
        print(j, end=" ")
        # если j больше либо равен 5 - прервать выполнение внутреннего цикла
        if j >= 5:
            print("внутренний цикл закончился с помощью break")
            break
    else:
        print("внутрненний цикл закончился сам")
else:
    print("внешний цикл закончился сам")

print("\n\n@@@@@@@@@@@@\n")
