print("Цикл While:")

# while условие:
#     блок инструкций

i = 0  # начальное
while i < 10:  # условие выполнения (включает конечное значение)
    print(i)
    i += 1  # изменение счетчитка

print("\n@@@@@@@@@@@@\n")

# условие выполнения можно задать и переменно типа bool
# в данном случае условия всегда истина - значит цикл обязательно начнется
condition = True
char = "a"
while condition:
    # ord() - получить номер (код) символа
    # chr() - получить символ по его номеру (коду)
    # char - символ
    # str(ord(char)) - код символа (число) приведенный к строке
    print("Char: " + char, "code: " + str(ord(char)))
    # получаем следующий символ по номеру исходя из номера предыдущего
    char = chr(ord(char) + 1)
    # задаем condition новое значение
    # проверяем, что текущий char (символ) все еще меньше 'z'
    condition = char <= "z"

print("\n\n@@@@@@@@@@@@\n")

